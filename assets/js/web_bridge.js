const METAMASK_DEEPLINK = 'metamask://';
const APP_SCHEME = 'myapp'; // Replace with your app's scheme
const APP_HOST = 'callback';

// Initialize Web3 with MetaMask Mobile
async function initializeMetaMask() {
    try {
        // Check if running in mobile environment
        const isMobile = /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
        
        if (!isMobile) {
            throw new Error('Please use MetaMask mobile app');
        }

        // Prepare the deep link parameters
        const deepLink = {
            url: `${METAMASK_DEEPLINK}dapp/${window.location.host}`,
            callback: `${APP_SCHEME}://${APP_HOST}`,
            parameters: {
                action: 'connect',
                chainId: '0x5A2' // Polygon zkEVM Testnet
            }
        };

        // Create the MetaMask connection request
        const encodedDeepLink = encodeURIComponent(JSON.stringify(deepLink));
        const metamaskUrl = `${METAMASK_DEEPLINK}?link=${encodedDeepLink}`;

        // Listen for the callback from MetaMask
        return new Promise((resolve, reject) => {
            // Set up message listener for MetaMask response
            window.addEventListener('message', async function handleMessage(event) {
                if (event.data.type === 'METAMASK_RESPONSE') {
                    window.removeEventListener('message', handleMessage);
                    
                    if (event.data.error) {
                        reject(new Error(event.data.error));
                        return;
                    }

                    try {
                        // Initialize ethers provider with the MetaMask provider
                        provider = new ethers.providers.Web3Provider(event.data.provider);
                        signer = provider.getSigner();
                        
                        // Initialize contract
                        contract = new ethers.Contract(contractAddress, contractABI, signer);
                        
                        // Get the connected account
                        const accounts = await provider.listAccounts();
                        resolve(accounts[0]);
                    } catch (error) {
                        reject(error);
                    }
                }
            });

            // Open MetaMask app using deep link
            window.location.href = metamaskUrl;

            // Set timeout for user response
            setTimeout(() => {
                reject(new Error('MetaMask connection timeout'));
            }, 30000); // 30 second timeout
        });
    } catch (error) {
        throw error;
    }
}

// Handle chain switching for MetaMask Mobile
async function switchToPolygonZkEVM() {
    try {
        if (!provider) throw new Error('Provider not initialized');

        const chainId = '0x5A2'; // Polygon zkEVM Testnet
        const networkParams = {
            chainId: chainId,
            chainName: 'Polygon zkEVM Testnet',
            nativeCurrency: {
                name: 'ETH',
                symbol: 'ETH',
                decimals: 18
            },
            rpcUrls: ['https://polygon-zkevm-testnet.infura.io/v3/497503df1a3f4c2fbbc43841db6dcb4d'],
            blockExplorerUrls: ['https://testnet-zkevm.polygonscan.com']
        };

        try {
            await provider.send('wallet_switchEthereumChain', [{ chainId }]);
        } catch (error) {
            if (error.code === 4902) {
                await provider.send('wallet_addEthereumChain', [networkParams]);
            } else {
                throw error;
            }
        }
    } catch (error) {
        throw error;
    }
}

// NFT Minting function
async function uploadDataAsNFT(ipfsHash, metadata, encryptedKey) {
    try {
        if (!contract) throw new Error('Contract not initialized');
        
        const tx = await contract.mintNFT(ipfsHash, metadata, encryptedKey);
        const receipt = await tx.wait();
        return { hash: tx.hash, receipt };
    } catch (error) {
        throw error;
    }
}

// Access granting function
async function grantAccess(ipfsHash, recipientAddress, duration) {
    try {
        if (!contract) throw new Error('Contract not initialized');
        
        const expiration = Math.floor(Date.now() / 1000) + duration;
        const tx = await contract.grantAccess(ipfsHash, recipientAddress, expiration);
        const receipt = await tx.wait();
        return { hash: tx.hash, receipt };
    } catch (error) {
        throw error;
    }
}

